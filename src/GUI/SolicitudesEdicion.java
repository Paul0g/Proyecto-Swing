    private void cargarIDs() {
     DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");


    ColEmpleado cola = Global.getColaIDs();

    
    ColEmpleado copia = new ColEmpleado();
    while (!cola.estaVacia()) {
        Empleado emp = cola.desencolar();
        modelo.addRow(new Object[]{emp.getId()});
        copia.encolar(emp); 
    }

  
    while (!copia.estaVacia()) {
        cola.encolar(copia.desencolar());
    }

    jTable1.setModel(modelo);
}
    
    
    
    
    
